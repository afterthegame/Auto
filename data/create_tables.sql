CREATE TABLE users (
id INT PRIMARY KEY AUTO_INCREMENT,
login VARCHAR(30) NOT NULL,
password VARCHAR(30) NOT NULL,
fio VARCHAR(50) NOT NULL,
type INT NOT NULL 
);

CREATE TABLE brands (
id INT PRIMARY KEY AUTO_INCREMENT,
name VARCHAR(30) NOT NULL
) CHARACTER SET utf8 COLLATE utf8_general_ci;

CREATE TABLE models (
id INT PRIMARY KEY AUTO_INCREMENT,
name VARCHAR(30) NOT NULL,
brand_id INT NOT NULL,
category_id INT NOT NULL,
wear_factor INT NOT NULL,
year_factor INT NOT NULL,
region_factor INT NOT NULL,
FOREIGN KEY(brand_id) REFERENCES brands(id)
) CHARACTER SET utf8 COLLATE utf8_general_ci;

CREATE TABLE bodies (
id INT PRIMARY KEY AUTO_INCREMENT,
name VARCHAR(30) NOT NULL
) CHARACTER SET utf8 COLLATE utf8_general_ci;

CREATE TABLE wear_factors (
id INT PRIMARY KEY AUTO_INCREMENT,
val FLOAT(6, 3) NOT NULL
);

CREATE TABLE editions (
id INT PRIMARY KEY AUTO_INCREMENT,
year INT NOT NULL,
volume FLOAT(6, 3) NOT NULL,
price FLOAT(6, 2) NOT NULL,
model_id INT NOT NULL,
body_id INT NOT NULL,
FOREIGN KEY(model_id) REFERENCES models(id),
FOREIGN KEY(body_id) REFERENCES bodies(id)
);

CREATE TABLE year_factors (
id INT NOT NULL,
year INT NOT NULL,
val FLOAT(6, 3) NOT NULL,
PRIMARY KEY(id, year)
);

CREATE TABLE region_factors (
id INT PRIMARY KEY AUTO_INCREMENT,
low FLOAT(6, 3) NOT NULL,
high FLOAT(6, 3) NOT NULL,
another FLOAT(6, 3) NOT NULL
);

CREATE TABLE mileages (
id INT PRIMARY KEY AUTO_INCREMENT,
year INT NOT NULL,
val INT NOT NULL
);

CREATE TABLE mileage_factors (
id INT PRIMARY KEY AUTO_INCREMENT,
mass FLOAT(6, 3) NOT NULL,
difference INT NOT NULL,
val FLOAT(6, 3) NOT NULL,
direction INT NOT NULL
);

CREATE TABLE usage_conditions (
id INT PRIMARY KEY AUTO_INCREMENT,
name VARCHAR(100) NOT NULL,
val FLOAT(5, 2) NOT NULL
) CHARACTER SET utf8 COLLATE utf8_general_ci;

CREATE TABLE damages (
id INT PRIMARY KEY AUTO_INCREMENT,
name VARCHAR(100) NOT NULL,
val FLOAT(5, 2) NOT NULL
) CHARACTER SET utf8 COLLATE utf8_general_ci;

CREATE TABLE price_factors (
id INT PRIMARY KEY AUTO_INCREMENT,
year INT NOT NULL,
mass FLOAT(6,3) NOT NULL,
val FLOAT(5,2) NOT NULL
);

CREATE TABLE components (
id INT PRIMARY KEY AUTO_INCREMENT,
name VARCHAR(32) NOT NULL,
price FLOAT(8,2) NOT NULL,
model_id INT NOT NULL
) CHARACTER SET utf8 COLLATE utf8_general_ci;


CREATE TABLE equipments (
id INT PRIMARY KEY AUTO_INCREMENT,
name VARCHAR(32) NOT NULL,
price FLOAT(8,2) NOT NULL,
model_id INT NOT NULL
) CHARACTER SET utf8 COLLATE utf8_general_ci;

CREATE TABLE repair_cost (
id INT PRIMARY KEY AUTO_INCREMENT,
val FLOAT(8,2) NOT NULL,
model_id INT NOT NULL
);

CREATE TABLE materials (
id INT PRIMARY KEY AUTO_INCREMENT,
name VARCHAR(32) NOT NULL,
price FLOAT(8, 2) NOT NULL
) CHARACTER SET utf8 COLLATE utf8_general_ci;

CREATE TABLE categories (
id INT PRIMARY KEY AUTO_INCREMENT,
name VARCHAR(32) NOT NULL,
flag BOOLEAN NOT NULL
) CHARACTER SET utf8 COLLATE utf8_general_ci;

CREATE TABLE X (
id INT PRIMARY KEY AUTO_INCREMENT,
A FLOAT(5,3) NOT NULL,
B FLOAT(5,3) NOT NULL,
month INT NOT NULL,
val FLOAT(6,3) NOT NULL
);
